package com.springdata.hibernate.model;

public class Info {
	
	private int id;
	
	private String phone;

	public Info() {
		
	}
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}
	
	
	

}
